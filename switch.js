

var digit = prompt  ("enter any digit : ");


switch(digit){
        case "0":
         console.log("zero");
         break;
        case "1":
        console.log("one");
        break;
        case "2":
        console.log("two"); 
        break;
        case "3":
        console.log("three");
        break;
        case "4":
        console.log("four");
        break;
        case "5":
        console.log("five");
        break;
        default:
          console.log("not a digit")

}